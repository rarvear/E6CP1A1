# Utiliza álgebra booleana para hacer un refactoring de este código
# Para verificar la respuestas, puedes modificar los valores de a y b.

a = 'verdadero'
b = 'falso'

if a == 'verdadero' || b== 'falso'
    puts ':)'
else
  if b == 'verdadero' || a == 'falso'
    puts ':|'
  else
    puts ':('
  end
end
